        const dotsContainer = slider.querySelector('.slider-dots');
        
        // –ï—Å–ª–∏ –Ω–µ—Ç –∏–∑–æ–±—Ä–∞–∂–µ–Ω–∏–π –∏–ª–∏ –∏—Ö –º–µ–Ω—å—à–µ 2, –Ω–µ —Å–æ–∑–¥–∞–µ–º —Å–ª–∞–π–¥–µ—Ä
